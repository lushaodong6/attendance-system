package entity;

public class Student {
    private String stuNo;  // 学号
    private String name;   // 姓名

    // getter 和 setter 方法
    public String getStuNo() {
        return stuNo;
    }
    public void setStuNo(String stuNo) {
        this.stuNo = stuNo;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
}
